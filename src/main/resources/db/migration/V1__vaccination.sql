CREATE TABLE `citizens` (
    `id` bigint NOT NULL AUTO_INCREMENT,
    `name` varchar(255) NOT NULL,
    `zip` char(4) NOT NULL,
    `age` int NOT NULL,
    `email` varchar(200),
    `ssn` varchar(10) UNIQUE ,
    PRIMARY KEY(`id`)
);

CREATE TABLE `vaccines` (
    `id` bigint NOT NULL AUTO_INCREMENT,
    `name` varchar(20) UNIQUE,
    PRIMARY KEY (`id`)
);

CREATE TABLE `vaccinations` (
	`id` bigint NOT NULL AUTO_INCREMENT,
	`citizen_id` bigint NOT NULL,
	`vaccination_date` datetime NOT NULL,
	`status` VARCHAR(10),
	`note` VARCHAR(200),
	`vaccine_id` bigint,
	PRIMARY KEY (`id`),
	CONSTRAINT FK_citizen_vaccination FOREIGN KEY (`citizen_id`) REFERENCES citizens(`id`),
	CONSTRAINT FK_vaccine_vaccination FOREIGN KEY (`vaccine_id`) REFERENCES vaccines(`id`)

);

CREATE TABLE `cities` (
